HRESULT
CElement::get_document(IDispatch ** ppIDoc)
{
    HRESULT hr = CTL_E_METHODNOTAPPLICABLE;
    CDoc * pDoc = Doc();

    if (!ppIDoc)
    {
        hr = E_POINTER;
        goto Cleanup;
    }

    *ppIDoc = NULL;

    if (pDoc)
    {
        CMarkup *pMarkup;
        hr = THR(EnsureInMarkup());
        if (hr)
            goto Cleanup;
        
        pMarkup = GetMarkup();
        Assert(pMarkup);

        hr = THR_NOTRACE(pMarkup->QueryInterface(IID_IHTMLDocument2, (void**)ppIDoc));
    }

Cleanup:
    RRETURN(SetErrorInfo(hr));
}

